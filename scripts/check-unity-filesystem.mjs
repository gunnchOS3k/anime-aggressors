#!/usr/bin/env node
/**
 * Unity filesystem case-sensitivity check (environment rescue).
 *
 * Unity refuses to open projects on case-sensitive filesystems:
 *   "The project is on case sensitive file system. Case sensitive file
 *    systems are not supported at the moment."
 *
 * This script detects that condition BEFORE anyone wastes time opening the
 * editor, by creating two files whose names differ only by case in a temp
 * directory under the Unity project parent.
 *
 * Exit codes: 0 = case-insensitive (OK for Unity), 3 = case-sensitive
 * (blocker — do not attempt Unity import), 1 = unexpected failure.
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const unityParent = path.join(root, "unity");
const unityProject = path.join(unityParent, "AnimeAggressorsUnity");

const testDir = path.join(unityParent, `.fs-case-test-${process.pid}`);
const upperFile = path.join(testDir, "CaseTest.tmp");
const lowerFile = path.join(testDir, "casetest.tmp");

console.log(`repo path:          ${root}`);
console.log(`unity project path: ${unityProject}`);

let caseSensitive = null;
let error = null;

try {
  fs.mkdirSync(testDir, { recursive: true });
  fs.writeFileSync(upperFile, "UPPER");
  fs.writeFileSync(lowerFile, "lower");

  // On a case-insensitive FS the second write clobbers the first: only one
  // directory entry exists and CaseTest.tmp now reads "lower".
  const entries = fs.readdirSync(testDir).filter((f) => f.toLowerCase() === "casetest.tmp");
  const upperContent = fs.readFileSync(upperFile, "utf8");
  caseSensitive = entries.length === 2 && upperContent === "UPPER";
} catch (e) {
  error = String(e);
} finally {
  fs.rmSync(testDir, { recursive: true, force: true });
}

if (error) {
  console.error(`ERROR: filesystem test failed: ${error}`);
  process.exit(1);
}

console.log(`CASE_SENSITIVE_FS=${caseSensitive}`);

const report = {
  generated_at: new Date().toISOString(),
  repo_path: root,
  unity_project_path: unityProject,
  test_dir: testDir,
  case_sensitive_fs: caseSensitive,
  unity_import_allowed: !caseSensitive,
  required_user_action: caseSensitive
    ? "Move/clone the repo to a case-insensitive APFS location (recommended: ~/UnityProjects/anime-aggressors). If the home volume is also case-sensitive, use a case-insensitive APFS disk image at /Volumes/UnityAA. See docs/UNITY_MAC_ENVIRONMENT_RESCUE.md."
    : "None — filesystem is case-insensitive, Unity can open this project location.",
};

fs.mkdirSync(path.join(root, "tmp"), { recursive: true });
fs.writeFileSync(path.join(root, "tmp/unity-filesystem-report.json"), JSON.stringify(report, null, 2) + "\n");

const md = `# Unity Filesystem Report

> Generated by \`scripts/check-unity-filesystem.mjs\` — ${report.generated_at}

| Check | Value |
|-------|-------|
| Repo path | \`${report.repo_path}\` |
| Unity project path | \`${report.unity_project_path}\` |
| CASE_SENSITIVE_FS | **${report.case_sensitive_fs}** |
| Unity import allowed | **${report.unity_import_allowed}** |

## Required action

${report.required_user_action}
${caseSensitive ? `
## How to fix

Unity does not support case-sensitive filesystems. Do **not** attempt Unity
import from this location. Follow \`docs/UNITY_MAC_ENVIRONMENT_RESCUE.md\`:

1. **Option A (preferred):** clone into a normal Mac user folder:
   \`~/UnityProjects/anime-aggressors\`
2. **Option B (if the home volume is also case-sensitive):** create a
   case-insensitive APFS disk image and clone into \`/Volumes/UnityAA\`.
` : ""}`;

fs.writeFileSync(path.join(root, "docs/UNITY_FILESYSTEM_REPORT.md"), md);

console.log("wrote tmp/unity-filesystem-report.json");
console.log("wrote docs/UNITY_FILESYSTEM_REPORT.md");

if (caseSensitive) {
  console.error("");
  console.error("BLOCKER: this repo is on a CASE-SENSITIVE filesystem. Unity cannot open it here.");
  console.error("Do NOT run Unity import. Move/clone the repo to a case-insensitive APFS location:");
  console.error("  mkdir -p ~/UnityProjects && cd ~/UnityProjects && git clone https://github.com/gunnchOS3k/anime-aggressors.git");
  console.error("If ~/ is also case-sensitive, create a case-insensitive APFS image at /Volumes/UnityAA.");
  console.error("Full instructions: docs/UNITY_MAC_ENVIRONMENT_RESCUE.md");
  process.exit(3);
}

console.log("Filesystem is case-insensitive — Unity can open the project from this location.");
