#!/usr/bin/env node
/**
 * Unity Editor install integrity check (environment rescue).
 *
 * Verifies the macOS Unity Hub app, the 6000.0.23f1 editor binary, and the
 * Unity Package Manager local server binary. A missing UnityPackageManager
 * with a present editor means the install is corrupt/incomplete:
 *   "Could not find Unity Package Manager local server application at: ..."
 *
 * The only supported fix is uninstall + reinstall through Unity Hub.
 * This script never attempts package-manager workarounds.
 *
 * Exit codes: 0 = install looks intact, 3 = corrupt install detected,
 * 2 = editor not installed at the expected path.
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");

const EDITOR_VERSION = "6000.0.23f1";
const HUB_APP = "/Applications/Unity Hub.app";
const EDITOR_BIN = `/Applications/Unity/Hub/Editor/${EDITOR_VERSION}/Unity.app/Contents/MacOS/Unity`;
const UPM_SERVER = `/Applications/Unity/Hub/Editor/${EDITOR_VERSION}/Unity.app/Contents/Resources/PackageManager/Server/UnityPackageManager`;

const hubDetected = fs.existsSync(HUB_APP);
const editorDetected = fs.existsSync(EDITOR_BIN);
const upmDetected = fs.existsSync(UPM_SERVER);
const installCorrupt = editorDetected && !upmDetected;

const rows = [
  ["Unity Hub", HUB_APP, hubDetected],
  ["Unity Editor binary", EDITOR_BIN, editorDetected],
  ["UnityPackageManager server", UPM_SERVER, upmDetected],
];

for (const [label, p, present] of rows)
  console.log(`${present ? "OK   " : "MISS "} ${label}: ${p}`);

console.log(`UNITY_EDITOR_INSTALL_CORRUPT=${installCorrupt}`);

let requiredAction;
if (installCorrupt) {
  requiredAction =
    `Unity ${EDITOR_VERSION} is installed but its UnityPackageManager server is missing — the install is corrupt/incomplete. ` +
    `Uninstall and reinstall Unity ${EDITOR_VERSION} through Unity Hub (Installs → gear → Uninstall, then reinstall). ` +
    `Do not attempt package-manager workarounds. See docs/UNITY_MAC_ENVIRONMENT_RESCUE.md Step 3.`;
} else if (!hubDetected) {
  requiredAction = "Install Unity Hub: brew install --cask unity-hub (or https://unity.com/download).";
} else if (!editorDetected) {
  requiredAction = `Install Unity ${EDITOR_VERSION} through Unity Hub (see docs/UNITY_LOCAL_RUNBOOK.md step 5).`;
} else {
  requiredAction = "None — Unity Hub, editor, and UnityPackageManager all detected.";
}

const report = {
  generated_at: new Date().toISOString(),
  editor_version: EDITOR_VERSION,
  unity_hub_path: HUB_APP,
  unity_hub_detected: hubDetected,
  unity_editor_path: EDITOR_BIN,
  unity_editor_detected: editorDetected,
  unity_package_manager_path: UPM_SERVER,
  unity_package_manager_detected: upmDetected,
  unity_editor_install_corrupt: installCorrupt,
  unity_import_allowed: hubDetected && editorDetected && upmDetected,
  required_user_action: requiredAction,
};

fs.mkdirSync(path.join(root, "tmp"), { recursive: true });
fs.writeFileSync(path.join(root, "tmp/unity-editor-integrity-report.json"), JSON.stringify(report, null, 2) + "\n");

const md = `# Unity Editor Integrity Report

> Generated by \`scripts/check-unity-editor-integrity.mjs\` — ${report.generated_at}

Target editor: **${EDITOR_VERSION}**

| Component | Path | Detected |
|-----------|------|----------|
| Unity Hub | \`${HUB_APP}\` | **${hubDetected}** |
| Unity Editor binary | \`${EDITOR_BIN}\` | **${editorDetected}** |
| UnityPackageManager server | \`${UPM_SERVER}\` | **${upmDetected}** |

| Verdict | Value |
|---------|-------|
| UNITY_EDITOR_INSTALL_CORRUPT | **${installCorrupt}** |
| Unity import allowed | **${report.unity_import_allowed}** |

## Required action

${requiredAction}
${installCorrupt ? `
## How to fix (reinstall only — no workarounds)

1. Open Unity Hub → **Installs**.
2. On \`${EDITOR_VERSION}\`, gear icon → **Uninstall**.
3. Reinstall \`${EDITOR_VERSION}\` (Hub Archive, or headless — see
   \`docs/UNITY_MAC_ENVIRONMENT_RESCUE.md\` Step 3).
4. Re-run \`node scripts/check-unity-editor-integrity.mjs\`.

Do **not** copy UnityPackageManager from another install, symlink around it,
or edit the Unity.app bundle.
` : ""}`;

fs.writeFileSync(path.join(root, "docs/UNITY_EDITOR_INTEGRITY_REPORT.md"), md);

console.log("wrote tmp/unity-editor-integrity-report.json");
console.log("wrote docs/UNITY_EDITOR_INTEGRITY_REPORT.md");

if (installCorrupt) {
  console.error("");
  console.error(`BLOCKER: Unity ${EDITOR_VERSION} install is corrupt (UnityPackageManager missing).`);
  console.error("Do NOT run Unity import. Uninstall and reinstall the editor through Unity Hub.");
  process.exit(3);
}
if (!editorDetected) {
  console.error("");
  console.error(`Unity ${EDITOR_VERSION} not installed at the expected path.`);
  process.exit(2);
}
console.log("Unity Editor install looks intact.");
